    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        String decryptedText = txtDecrypted.getText();
        if(decryptedText.length() > 30){
            lblError.setText("Your decrypted message cannot be longer than 30 characters");
        } else{
            String encryptedText = reverseText(decryptedText);
            encryptedText = ceasarCipher(encryptedText, true);
            encryptedText = scrambleLetters(encryptedText);
            // Display result
            lblEncryptedText.setText("Encrypted text: " + encryptedText);
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

