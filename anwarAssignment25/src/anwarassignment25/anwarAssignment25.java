    private String reverseText(String text){
        String reversedText = "";
        for(int i = text.length()-1; i >= 0; i--){
            reversedText += text.charAt(i);
        }
        return reversedText;
    }
    
    private String ceasarCipher(String text, boolean encrypt){
        String cipheredText = "";        
        for(int i = 0; i < text.length(); i++){
            char txtCharacter = text.charAt(i);
            int asciiValue = (int)txtCharacter ;
            if(encrypt){
                //65 - 90 capital ltters
                //97 - 122 lowercase letters
                if(asciiValue >= 32 && asciiValue <= 79){
                    asciiValue += 47;
                } else if(asciiValue >= 80 && asciiValue <= 126){
                    asciiValue -= 48;
                } 
            } else{
                if(asciiValue >= 32 && asciiValue <= 78){
                      asciiValue += 48;
                } else if(asciiValue >= 79 && asciiValue <= 126){
                    asciiValue -= 47;
                } 
            }
            txtCharacter = (char)asciiValue;
            cipheredText += txtCharacter;
        }
        return cipheredText;
    }
    
    private String scrambleLetters(String text){
        String firstRandomString = "";
        String secRandomString = "";
        // Used to determine which string to append to
        int switchNumber = 0;
        // TODO: Can I have 18 characters if max limit is 31
        // 18 characters per string, because each iteration the string being appended to is switched
        for (int i = 0; i <= 36; i = i + 1){
            // Generate a random integer between 33 and 126
            int randomAscii =(int)Math.round(Math.random()*25+1+96);
            // Convert random ASCII int to a character
            char randomLetter = (char)randomAscii;
            
            // Append character to string, the string appended to is switched every time
            if(switchNumber == 0){
                firstRandomString += randomLetter;     
                switchNumber = 1;
            } else if(switchNumber == 1){
                secRandomString += randomLetter;     
                switchNumber = 0;
            }         
        }
        text = firstRandomString + text + secRandomString;
        return text;
    }  
    
    private String unScrambleLetters(String text){
        // Replace first scrambled string
        text = text.replace(text.substring(0, 18), "");
        // Replace second scrambled string
        text = text.replace(text.substring(text.length()-19,text.length()), "");
        return text;
    }
